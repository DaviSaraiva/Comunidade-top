                        Projeto Gráfico é um conjunto de elementos que formam e dão características a meio de informação.
Um Projeto Gráfico é constituído de uma série de plataformas que formam a sua lógica construtiva, assim definindo o seu aspecto visual, e bem como seu aspecto editorial.
Aspecto Visual: cores, tipografia, design, imagem, …
Aspecto Editorial: texto, linguagem, conteúdo, …
Um bom designer consegue desenvolver Projetos Gráficos nestes dois aspectos, com objetividade, simplicidade e inteligência.
Mas para que este Projeto Prático tome forma é necessário antecipar uma série de perguntas junto ao cliente, o que chama de briefing.
Briefing é o resumo de informações sobre a empresa, e enfatiza questões que servirão de ferramentas relevantes ao projeto.
Todo o início do desenvolvimento do projeto é feito uma reunião, sendo em cada passo, com o cliente. O projeto gráfico é acompanhado desde a prospecção do cliente ao fim, com direito a visita. Vale ressaltar que nenhum projeto gráfico físico é feito sem um protótipo, aquele, óbvio, que caiba em sua necessidade.
Quer saber como preparar um bom briefing? Nossa Titã deixa aí do lado um material ensinando passo a passo com dicas bem bacanas.

